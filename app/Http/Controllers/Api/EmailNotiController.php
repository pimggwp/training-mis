    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $data = array('email' => $request->get('email'), 'name' => $request->get('name'), 'event_title' => $request->get('event_title'));
        Mail::send('emails.email', ['data' => $data], function ($message) use ($data) {
            $message->subject('กำหนดการใหม่จากสหกรณ์โรงเรียนอนุบาลเชียงคำ');
            $message->to($data['email'], $data['name']);
        });
    }

